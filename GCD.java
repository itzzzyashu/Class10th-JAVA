// To calculate the GCD of two number
import java.util.*;
public class GCD
{
  public static void main(String args[])
  {
    Scanner in=new Scanner(System.in);
    int a,b,i,p,gcd=0;
    System.out.println("Enter Two Numbers");
    a=in.nextInt();
    b=in.nextInt();
    p*b;
    for(i=1;i<p;p++)
    {
      if(a%i==0 && b%i==0)
        gcd=i;
    }
  }
}
    
